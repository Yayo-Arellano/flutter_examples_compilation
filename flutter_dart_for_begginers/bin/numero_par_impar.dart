// Crear un programa que dado un número nos diga si es un número par o impar
void main() {
  final numero = 0;

  // Los numeros pares son los numeros que divididos entre dos su residuo/resto es igual a cero.

  if (numero % 2 == 0) {
    print('El $numero es par');
  } else {
    print('El $numero es impar');
  }
}
