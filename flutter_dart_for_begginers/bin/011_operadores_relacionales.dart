void main() {
  // Operadores de igualdad
  var a = 10;
  var b = 15;

  if (a == b) {
    print('Son iguales');
  }
  if (a != b) {
    print('Son desiguales');
  }

  // Operadores relacionales
  if (a > b) {
    print('a es mayor que b');
  }

  if (a < b) {
    print('a es menor que b');
  }

  if (a >= b) {
    print('a es mayor o igual que b');
  }

  if (a <= b) {
    print('a es menor o igual que b');
  }
}
