/// Operadores aritmeticos con asignacion. Notacion abreviada

void main() {
  int num = 5;
  print( num++ ); // 5
  print ( num ); // 6

  int num2 = 5;
  print( ++num2 ); // 6
  print ( num2 ); // 6

  int num3 = 5;
  print( num3-- ); // 5
  print ( num3 ); // 4

  int num4 = 5;
  print( --num4 ); // 4
  print ( num4 ); // 4
}
