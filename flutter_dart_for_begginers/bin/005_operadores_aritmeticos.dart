
/// Operadores aritmeticos

void main() {
  // Ahora vamos a ver los 5 operadores aritmeticos
  // basicos que son suma, resta, mul,
  // div division con resultado entero y modulo.
  // El resultaod del modulo es el resuido/resto
  // de la division

  final numero1 = 5;
  final numero2 = 2;

  var suma = numero1 + numero2;
  var resta = numero1 - numero2;
  var multiplicacion = numero1 * numero2;
  var division = numero1 / numero2;
  var divisionEntero = numero1 ~/ numero2;
  var resto = numero1 % numero2;

  print('La suma es $suma');
  print('La resta es $resta');
  print('La multiplicacion es $multiplicacion');
  print('La division es $division');
  print('La division entero es $divisionEntero');
  print('El modulo es $resto');


}
